from __future__ import annotations


class EditHistory:
    def __init__(self):
        self._undo_stack = []
        self._redo_stack = []

    @property
    def can_undo(self) -> bool:
        return bool(self._undo_stack)

    @property
    def can_redo(self) -> bool:
        return bool(self._redo_stack)

    def clear(self) -> None:
        self._undo_stack.clear()
        self._redo_stack.clear()
